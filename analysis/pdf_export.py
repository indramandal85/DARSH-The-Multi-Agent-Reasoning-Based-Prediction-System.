from io import BytesIO
from datetime import datetime


PAGE_WIDTH = 595
PAGE_HEIGHT = 842
LEFT_MARGIN = 44
TOP_MARGIN = 50
BOTTOM_MARGIN = 50


def build_report_pdf_bytes(
    topic: str,
    report_markdown: str,
    outcome_probs: dict | None = None,
    market_impact: dict | None = None,
    population_model: dict | None = None,
) -> bytes:
    lines = _build_lines(topic, report_markdown, outcome_probs or {}, market_impact or {}, population_model or {})
    pages = _paginate(lines)
    return _render_pdf(pages)


def _build_lines(topic: str, report_markdown: str, outcome_probs: dict, market_impact: dict, population_model: dict) -> list[tuple[str, str]]:
    lines: list[tuple[str, str]] = [
        ("title", "DARSH Prediction Report"),
        ("meta", f"Topic: {topic}"),
        ("meta", f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}"),
        ("blank", ""),
        ("heading", "Prediction Snapshot"),
    ]

    dominant_outcome = ""
    dominant_prob = 0
    if outcome_probs:
        dominant_outcome = max(outcome_probs, key=outcome_probs.get)
        dominant_prob = outcome_probs[dominant_outcome]

    snapshot_rows = [
        f"Dominant outcome: {dominant_outcome or 'n/a'}",
        f"Dominant probability: {dominant_prob:.1f}%" if dominant_outcome else "Dominant probability: n/a",
        f"Market regime: {market_impact.get('market_regime', 'n/a')}",
        f"Volatility expectation: {market_impact.get('volatility_expectation', 'n/a')}",
    ]
    if population_model:
        snapshot_rows.append(f"Population read: {population_model.get('dominant_population_outcome', 'n/a')}")
    for row in snapshot_rows:
        lines.append(("body", row))

    if outcome_probs:
        lines.extend([("blank", ""), ("heading", "Outcome Distribution")])
        for outcome, prob in sorted(outcome_probs.items(), key=lambda item: item[1], reverse=True):
            lines.append(("body", f"{outcome.capitalize()}: {prob:.1f}%"))

    strengthen = _collect_trigger_lines(market_impact, "strengthens")
    weaken = _collect_trigger_lines(market_impact, "weakens")
    if strengthen or weaken:
        lines.extend([("blank", ""), ("heading", "Market Triggers")])
        if strengthen:
            lines.append(("subheading", "Strengthens"))
            for item in strengthen:
                lines.append(("body", f"- {item}"))
        if weaken:
            lines.append(("subheading", "Weakens"))
            for item in weaken:
                lines.append(("body", f"- {item}"))

    if report_markdown.strip():
        lines.extend([("blank", ""), ("heading", "Detailed Report")])
        for raw in report_markdown.splitlines():
            line = _clean_markdown_line(raw)
            if not line:
                lines.append(("blank", ""))
            elif raw.startswith("## "):
                lines.append(("subheading", line))
            elif raw.startswith("# "):
                continue
            else:
                lines.append(("body", line))

    return lines


def _collect_trigger_lines(market_impact: dict, bucket: str) -> list[str]:
    trigger_map = market_impact.get("confidence_triggers") or {}
    items = trigger_map.get(bucket) or []
    if items:
        return [str(item) for item in items[:6]]

    fallback = market_impact.get("monitoring_signals") or []
    return [str(item) for item in fallback[:4]] if bucket == "strengthens" else []


def _clean_markdown_line(line: str) -> str:
    clean = line.strip()
    if not clean:
        return ""
    clean = clean.replace("**", "")
    if clean.startswith("*Brier Score:") or clean.startswith("*Generated by"):
        clean = clean.strip("*")
    return clean


def _paginate(lines: list[tuple[str, str]]) -> list[list[tuple[str, str]]]:
    pages: list[list[tuple[str, str]]] = [[]]
    remaining_height = PAGE_HEIGHT - TOP_MARGIN - BOTTOM_MARGIN

    for kind, text in lines:
        wrapped = _wrap_text(text, 92 if kind == "body" else 78)
        if kind == "blank":
            wrapped = [""]
        line_height = _line_height(kind)
        block_height = line_height * max(len(wrapped), 1)

        if block_height > remaining_height and pages[-1]:
            pages.append([])
            remaining_height = PAGE_HEIGHT - TOP_MARGIN - BOTTOM_MARGIN

        for segment in wrapped:
            pages[-1].append((kind, segment))
            remaining_height -= line_height

    return pages


def _wrap_text(text: str, limit: int) -> list[str]:
    if not text:
        return [""]
    words = text.split()
    if not words:
        return [""]

    lines = []
    current = words[0]
    for word in words[1:]:
        candidate = f"{current} {word}"
        if len(candidate) <= limit:
            current = candidate
        else:
            lines.append(current)
            current = word
    lines.append(current)
    return lines


def _line_height(kind: str) -> int:
    return {
        "title": 26,
        "heading": 20,
        "subheading": 16,
        "meta": 15,
        "blank": 10,
    }.get(kind, 14)


def _font_size(kind: str) -> int:
    return {
        "title": 22,
        "heading": 15,
        "subheading": 12,
        "meta": 10,
        "blank": 11,
    }.get(kind, 11)


def _render_pdf(pages: list[list[tuple[str, str]]]) -> bytes:
    objects: list[bytes] = []

    def add_object(data: str | bytes) -> int:
        payload = data.encode("latin-1", errors="replace") if isinstance(data, str) else data
        objects.append(payload)
        return len(objects)

    font_id = add_object("<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>")
    bold_font_id = add_object("<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica-Bold >>")

    page_ids = []
    content_ids = []

    for page in pages:
        content = _build_page_stream(page, font_id, bold_font_id)
        content_id = add_object(f"<< /Length {len(content)} >>\nstream\n".encode("latin-1") + content + b"\nendstream")
        content_ids.append(content_id)
        page_id = add_object("<< /Type /Page /Parent 0 0 R /MediaBox [0 0 595 842] /Resources << /Font << /F1 "
                             f"{font_id} 0 R /F2 {bold_font_id} 0 R >> >> /Contents {content_id} 0 R >>")
        page_ids.append(page_id)

    kids = " ".join(f"{pid} 0 R" for pid in page_ids)
    pages_id = add_object(f"<< /Type /Pages /Kids [{kids}] /Count {len(page_ids)} >>")
    catalog_id = add_object(f"<< /Type /Catalog /Pages {pages_id} 0 R >>")

    objects[pages_id - 1] = f"<< /Type /Pages /Kids [{kids}] /Count {len(page_ids)} >>".encode("latin-1")
    for page_id, content_id in zip(page_ids, content_ids):
        objects[page_id - 1] = (
            f"<< /Type /Page /Parent {pages_id} 0 R /MediaBox [0 0 595 842] "
            f"/Resources << /Font << /F1 {font_id} 0 R /F2 {bold_font_id} 0 R >> >> "
            f"/Contents {content_id} 0 R >>"
        ).encode("latin-1")

    output = BytesIO()
    output.write(b"%PDF-1.4\n%\xe2\xe3\xcf\xd3\n")
    offsets = [0]
    for index, obj in enumerate(objects, start=1):
        offsets.append(output.tell())
        output.write(f"{index} 0 obj\n".encode("latin-1"))
        output.write(obj)
        output.write(b"\nendobj\n")

    xref_pos = output.tell()
    output.write(f"xref\n0 {len(objects) + 1}\n".encode("latin-1"))
    output.write(b"0000000000 65535 f \n")
    for offset in offsets[1:]:
        output.write(f"{offset:010d} 00000 n \n".encode("latin-1"))

    output.write(
        f"trailer\n<< /Size {len(objects) + 1} /Root {catalog_id} 0 R >>\nstartxref\n{xref_pos}\n%%EOF".encode("latin-1")
    )
    return output.getvalue()


def _build_page_stream(page: list[tuple[str, str]], font_id: int, bold_font_id: int) -> bytes:
    y = PAGE_HEIGHT - TOP_MARGIN
    chunks = []
    for kind, text in page:
        size = _font_size(kind)
        font_name = "F2" if kind in {"title", "heading", "subheading"} else "F1"
        y -= _line_height(kind)
        if kind == "blank":
            continue
        escaped = _pdf_escape(text)
        chunks.append(f"BT /{font_name} {size} Tf {LEFT_MARGIN} {y} Td ({escaped}) Tj ET")
    return "\n".join(chunks).encode("latin-1", errors="replace")


def _pdf_escape(text: str) -> str:
    return text.replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)")
